function solve(){
    let firstNumber = 7; // Чрез използването на "let" числата се запазват!
    let secondNumber = 8; // "camel case" - така се нарича начина на записване на променливите!
                        // Първата буква от името на променливата е малка, а втората с голяма буква!
    firstNumber = 10;   // Генералната идея на променливата!!!
    console.log(firstNumber * secondNumber);
}

solve("5")